package com.waffle.vo;

public class RoomVO {
	private String memId;
	private String serviceId;
	private String serviceName;
	private String roomNumber;
	private String tv;
	private String light;
	private String aircon;
	private String window;
	private String innerTemp;
	private String innerWet;
	
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getServiceId() {
		return serviceId;
	}
	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getRoomNumber() {
		return roomNumber;
	}
	public void setRoomNumber(String roomNumber) {
		this.roomNumber = roomNumber;
	}
	public String getTv() {
		return tv;
	}
	public void setTv(String tv) {
		this.tv = tv;
	}
	public String getLight() {
		return light;
	}
	public void setLight(String light) {
		this.light = light;
	}
	public String getAircon() {
		return aircon;
	}
	public void setAircon(String aircon) {
		this.aircon = aircon;
	}
	public String getWindow() {
		return window;
	}
	public void setWindow(String window) {
		this.window = window;
	}
	public String getInnerTemp() {
		return innerTemp;
	}
	public void setInnerTemp(String innerTemp) {
		this.innerTemp = innerTemp;
	}
	public String getInnerWet() {
		return innerWet;
	}
	public void setInnerWet(String innerWet) {
		this.innerWet = innerWet;
	}
	
	
}
